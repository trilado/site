<div class="page-header">
	<h1>Sobre o Trilado</h1>
</div>
<p>O Trilado é um framework PHP que segue a arquitetura de Padrão de Projeto MVC, que visa à separação da lógica de negócio, o visual e fluxo da aplicação. Criando com o objetivo de facilitar o desenvolvimento de aplicações web em PHP, o Trilado é grátis, liberado sob a Licença New BSD, que pode ser utilizado para desenvolver aplicações comerciais.</p>
<p>O Trilado contém uma estrutura organizada, capaz de desenvolver aplicações web com poucas linhas de código, tornando o desenvolvimento e a manutenção fáceis e rápidos e com menos chances de erros. Além do MVC, utiliza Padrões de Projeto conhecidos, como o Singleton, Active Record e Front Controller, proporcionando também recursos para o trabalho em equipe.</p>

<h2>História</h2>
<p>O projeto do Trilado Framework foi iniciado em outubro de 2009, por Valdirene Neves, mas somente agora, em 2012, que o framework ganhou uma grande divulgação.</p>
<p>O nome do framework teve como origem o próprio modelo adotado, ou seja, “Trilado” representa os “três lados” do padrão (Model-View-Controller).</p>

<h2>Créditos</h2>
<p>Algumas partes do Trilado Framework foi inspirado em partes de outros frameworks existentes, veja a lista abaixo:</p>
<ul>
	<li><a href="http://www.asp.net/mvc" target="_blank">ASP.NET MVC</a>: Esse foi a principal fonte de inspiração do Trilado, principalmente na forma em que trabalha com os retornos na actions;</li>
	<li><a href="http://vorticephp.com/" target="_blank">Vórtice</a>: Um pouco da estrutura de diretórios e segurança.</li>
</ul>

