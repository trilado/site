<h2>História</h2>
<p>O projeto do Trilado Framework foi iniciado em outubro de 2009, por Valdirene Neves, mas somente agora, em 2012, que o framework ganhou uma grande divulgação. O nome do framework teve como origem o próprio modelo adotado, ou seja, "Trilado" representa os "três lados" do padrão (Model-View-Controller).</p>
<p>A sua primeira versão era bastante limitada, contendo poucos recursos. Mas no final de 2011, o Trilado começou a ser reescrito, com código mais limpo e otimizado, sem deixar de lado seu objetivo, simplecidade e facilidade para os desenvolvedores o utilizarem.</p>

<h2>Créditos</h2>
<p>Algumas partes do Trilado Framework foi inspirado em partes de outros frameworks existentes, veja a lista abaixo:</p>
<ul>
	<li><a href="http://www.asp.net/mvc" target="_blank">ASP.NET MVC</a>: Esse foi a principal fonte de inspiração do Trilado, principalmente na forma em que trabalha com os retornos na actions;</li>
	<li><a href="http://vorticephp.com/" target="_blank">Vórtice</a>: Um pouco da estrutura de diretórios e segurança.</li>
</ul>