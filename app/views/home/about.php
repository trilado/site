	<h1>Sobre o Trilado</h1>
</div>
<p>O Trilado é um framework PHP que segue a arquitetura de Padrão de Projetos MVC, que visa à separação da lógica de negócio, o visual e fluxo da aplicação. Criando com o objetivo de facilitar o desenvolvimento de aplicações web em PHP, o Trilado é grátis, liberado sob a Licença New BSD, que pode ser utilizado para desenvolver aplicações comerciais.</p>
<p>O Trilado contém uma estrutura organizada, capaz de desenvolver aplicações web com poucas linhas de código, tornando o desenvolvimento e a manutenção fáceis e rápidos e com menos chances de erros. Além do MVC, utiliza Padrões de Projeto conhecidos, como o Singleton, Active Record e Front Controller, proporcionando também recursos para o trabalho em equipe.</p>
