<div class="page-header">
	<h1>Características</h1>
</div>

<div class="row-fluid">
	<div class="span3"><h4>Comercial</h4><p>O Trilado Framework é grátis e pode ser utilizado no desenvolvimento de aplicações comerciais.</p></div>
	<div class="span3"><h4>Português</h4><p>A documentação é totalmente em português, pois foi desenvolvido por brasileiros para brasileiros.</p></div>
	<div class="span3"><h4>Rápido</h4><p>Guarda informações em cache e só carrega os arquivos que serão utilizados na requisição.</p></div>
	<div class="span3"><h4>Seguro</h4><p>Contém filtro dos dados, sessões criptografadas, validação de dados e configuração de erros, que podem ser apresentados somente no localhost.</p></div>
</div>

<div class="row-fluid">
	<div class="span3"><h4>Anotações</h4><p>Define metadados sobre os models e controllers, como por exemplo, validação e autenticação automática.</p></div>
	<div class="span3"><h4>Multidatabase</h4><p>Conecta em vários de tipos de banco de dados diferentes, necessitando apenas ter o <code>Datasource</code> referente a base de dados. O Trilado já vem com suporte ao MySQL e SQL Server.</p></div>
	<div class="span3"><h4>ORM</h4><p>Com o Mapeamento de Objeto-relacional, as tabelas do banco de dados são representadas por classes e os registros de cada tabela são representados como instâncias das classes correspondentes.</p></div>
	<div class="span3"><h4>Active Record</h4><p>As instâncias de <code>Model</code> á vêm com métodos para manipulação a sua manipulação, seguindo o conceituado padrão Active Record.</p></div>
</div>
<div class="row-fluid">
	<div class="span3"><h4>Cache</h4><p>Guarda informações, página e consultas SQL em cache, suportando configuração do tipo de cache.</p></div>
	<div class="span3"><h4>Autenticação</h4><p>Com o auxílio das Anotações, é possível autenticar as actions facilmente utilizando a marcação <code>@Auth("papel")</code>.</p></div>
	<div class="span3"><h4>Auto .json e .xml</h4><p>É configurável se as requisições com o endereço .json ou .xml devem retornar, automaticamente, o conteúdo de acordo com o formato.</p></div>
	<div class="span3"><h4>Auto AJAX</h4><p>É configurável se as requisições AJAX devem retornar, automaticamente, o conteúdo de JSON.</p></div>
</div>
<div class="row-fluid">
	<div class="span3"><h4>Internacionalização</h4><p>Uma aplicação pode ser apresentada ao usuário em vários idiomas, mudando apenas a URL.</p></div>
	<div class="span3"><h4>Versão Móvel</h4><p>Detecção automática de acesso via dispositível móvel e apresentação da view, se configurado, na versão móvel.</p></div>
	<div class="span3"><h4>URL's Amigáveis</h4><p>Por padrão, as URL's são amigáveis, que melhora o SEO do site.</p></div>
	<div class="span3"><h4>Rotas e Préfixos</h4><p>Utilizando expressões regulares, é possível reescrever as URL's. Além disso, pode, também, definir préfixos, como por exemplo 'admin'.</p></div>
</div>
<div class="row-fluid">
	<div class="span3"><h4>Multitemplate</h4><p>Você pode definir um template para cada action.</p></div>
	<div class="span3"><h4>HTML Helpers:</h4> Auxilia na criação de tags e formulários.</div>
	<div class="span3"><h4>Snippets</h4><p>No Trilado, os snippets são views utilizadas por vários controllers.</p></div>
	<div class="span3"><h4>Tipos de Retorno</h4><p>Action suporta vários tipos de retorno.</p></div>
</div>
<div class="row-fluid">
	<div class="span3"><h4>Logs</h4><p>Com o modo depuração desabilitado, os erros gerados são guardados em arquivos de logs.</p></div>
	<div class="span3"><h4>Registrar Diretórios</h4><p>Na importação automática de arquivos, o desenvolvedor pode definir diretórios para o Trilado importar esses arquivos.</p></div>
	<div class="span3"><h4>Hook</h4><p>Em alguns casos quando se faz necessário alterar o modo de funcionamento do Trilado. Usando hook não é necessário modificar o código-fonte do Framework.</p></div>
	<div class="span3"><h4>Erros Personalizados</h4><p>Basta criar uma view com o número do erro dentro do diretório reservado.</p></div>
</div>
<div class="row-fluid">
	<div class="span3"><h4>Módulos</h4><p>Suporte a criação de módulos (HMVC).</p></div>
	<div class="span3"><h4>Cache Automático</h4><p>Gerenciamento automático das consultados ao banco de dados atualizando o cache ao alterar um registro.</p></div>
</div>

